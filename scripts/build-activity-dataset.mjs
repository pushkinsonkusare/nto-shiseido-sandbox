/* =============================================================
 * build-activity-dataset.mjs
 *
 * Reads the DJI agent training dataset (.xlsx) and emits a typed
 * TypeScript table at src/catalog/activityDataset.generated.ts.
 *
 * The xlsx is NOT committed (it lives wherever you downloaded it);
 * the GENERATED table is committed so the app never depends on the
 * spreadsheet at runtime. Re-run this whenever the dataset changes:
 *
 *   node scripts/build-activity-dataset.mjs [path-to-xlsx]
 *
 * Default path: ~/Downloads/DJI_Agent_Training_Dataset_v2.xlsx
 *
 * Parsing approach: an .xlsx is a zip of XML. We shell out to
 * `unzip -p` for the worksheet XML and parse cells with regex —
 * the sheet uses inline strings (<is><t>…</t></is>), so no
 * sharedStrings table to resolve. Zero npm dependencies.
 * ============================================================= */

import { execFileSync } from "node:child_process";
import { writeFileSync } from "node:fs";
import { homedir } from "node:os";
import { join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = fileURLToPath(new URL(".", import.meta.url));
const REPO_ROOT = resolve(__dirname, "..");

const xlsxPath =
  process.argv[2] ??
  join(homedir(), "Downloads", "DJI_Agent_Training_Dataset_v2.xlsx");

const OUT_PATH = join(REPO_ROOT, "src", "catalog", "activityDataset.generated.ts");

function readSheetXml(path) {
  return execFileSync("unzip", ["-p", path, "xl/worksheets/sheet1.xml"], {
    encoding: "utf8",
    maxBuffer: 32 * 1024 * 1024,
  });
}

function parseRows(xml) {
  const rowChunks = xml.match(/<row[^>]*>(.*?)<\/row>/gs) ?? [];
  return rowChunks.map((rowXml) => {
    const cellChunks = rowXml.match(/<c\b[^>]*>.*?<\/c>|<c\b[^>]*\/>/gs) ?? [];
    return cellChunks.map((c) => {
      const inline = c.match(/<t[^>]*>(.*?)<\/t>/s);
      if (inline) {
        return decodeXml(inline[1].replace(/<[^>]+>/g, ""));
      }
      const v = c.match(/<v>(.*?)<\/v>/s);
      return v ? decodeXml(v[1]) : "";
    });
  });
}

function decodeXml(s) {
  return s
    .replace(/&amp;/g, "&")
    .replace(/&lt;/g, "<")
    .replace(/&gt;/g, ">")
    .replace(/&quot;/g, '"')
    .replace(/&apos;/g, "'")
    .replace(/&#(\d+);/g, (_, d) => String.fromCharCode(Number(d)));
}

const COLUMNS = [
  "Activity",
  "Environment",
  "Skill_Level",
  "Budget_Tier",
  "Primary_Family",
  "Recommended_Product",
  "Secondary_Family",
  "Secondary_Product",
  "Accessory_Categories",
  "Cost_Effective_Kit",
  "Ideal_Kit",
  "Pro_Kit",
];

function tsString(s) {
  return JSON.stringify(s ?? "");
}

function main() {
  const xml = readSheetXml(xlsxPath);
  const rows = parseRows(xml);
  const header = rows[0];
  for (let i = 0; i < COLUMNS.length; i += 1) {
    if (header[i] !== COLUMNS[i]) {
      throw new Error(
        `Header mismatch at col ${i}: expected "${COLUMNS[i]}", got "${header[i]}". ` +
          `Dataset schema changed — update COLUMNS in this script.`,
      );
    }
  }
  const body = rows.slice(1).filter((r) => r.some((c) => c !== ""));

  const records = body.map((r) => {
    const obj = {};
    COLUMNS.forEach((col, i) => {
      obj[col] = r[i] ?? "";
    });
    return obj;
  });

  const lines = [];
  lines.push("/* AUTO-GENERATED by scripts/build-activity-dataset.mjs — DO NOT EDIT BY HAND.");
  lines.push(" * Source: DJI agent training dataset (.xlsx). Re-run the generator to refresh.");
  lines.push(" */");
  lines.push("");
  lines.push('import type { ActivityDatasetRow } from "./activityDataset";');
  lines.push("");
  lines.push(`export const ACTIVITY_DATASET_ROWS: ActivityDatasetRow[] = [`);
  for (const rec of records) {
    lines.push("  {");
    lines.push(`    activity: ${tsString(rec.Activity)},`);
    lines.push(`    environment: ${tsString(rec.Environment)},`);
    lines.push(`    skillLevel: ${tsString(rec.Skill_Level)},`);
    lines.push(`    budgetTier: ${tsString(rec.Budget_Tier)},`);
    lines.push(`    primaryFamily: ${tsString(rec.Primary_Family)},`);
    lines.push(`    recommendedProduct: ${tsString(rec.Recommended_Product)},`);
    lines.push(`    secondaryFamily: ${tsString(rec.Secondary_Family)},`);
    lines.push(`    secondaryProduct: ${tsString(rec.Secondary_Product)},`);
    lines.push(`    accessoryCategories: ${tsString(rec.Accessory_Categories)},`);
    lines.push(`    costEffectiveKit: ${tsString(rec.Cost_Effective_Kit)},`);
    lines.push(`    idealKit: ${tsString(rec.Ideal_Kit)},`);
    lines.push(`    proKit: ${tsString(rec.Pro_Kit)},`);
    lines.push("  },");
  }
  lines.push("];");
  lines.push("");

  writeFileSync(OUT_PATH, lines.join("\n"), "utf8");
  // eslint-disable-next-line no-console
  console.log(
    `Wrote ${records.length} rows to ${OUT_PATH.replace(REPO_ROOT + "/", "")}`,
  );
}

main();
